void main() {
  // Declare an integer variable and perform arithmetic operations
  int x = 10;
  int y = 5;
  int sum = x + y;
  int difference = x - y;
  int product = x * y;
  double quotient = x / y;
  print('Sum: $sum');
  print('Difference: $difference');
  print('Product: $product');
  print('Quotient: $quotient');

  // Declare a double variable and perform calculations
  double a = 3.14;
  double b = 2.718;
  double c = a * b;
  double d = a / b;
  double e = a + b;
  double f = a - b;
  print('c: $c');
  print('d: $d');
  print('e: $e');
  print('f: $f');

  // Declare a string variable
  String name = "John Doe";
  print('Name: $name');

  // Declare a list variable
  List<String> fruits = ["apple", "banana", "orange"];
  print('Fruits: $fruits');

  // Declare a map variable with relevant examples
  Map<String, String> countries = {
    "Kenya": "Nairobi",
    "Tanzania": "Arusha",
    "Uganda": "Kampala"
  };
  print('Countries: $countries');
}